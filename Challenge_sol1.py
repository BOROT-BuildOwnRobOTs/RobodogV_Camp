#โจทย์ Challenge: สร้างปิรามิดแปลกๆ โดยใช้ Loop และ List
#คำอธิบายโจทย์
#เขียนโปรแกรมเพื่อสร้างปิรามิดที่มีความสูงตามที่ผู้ใช้ป้อนเข้ามา โดยข้อมูลในแต่ละชั้นจะอ่านมาจาก list ที่กำหนดไว้ล่วงหน้า. ปิรามิดนี้จะมีลักษณะพิเศษคือใช้สมาชิกใน list ตามลำดับที่ตั้งไว้ในแต่ละชั้น, โดยที่แต่ละชั้นเริ่มต้นด้วยสมาชิกแรกของ list และทำการวนกลับไปยังสมาชิกตั้งต้นเมื่อถึงขอบเขตของ list.

#ตัวอย่าง Input และ Output
#Input List: ['a', 'b', 'c', 'd']
#Input Height: 5
# รับค่าความสูงของปิรามิดจากผู้ใช้
height = int(input("Enter the height of the pyramid: "))

# กำหนด list ของข้อมูลที่จะใช้ในแต่ละชั้นของปิรามิด
data_list = ['a', 'b', 'c', 'd']

# สร้างปิรามิดโดยใช้ loop
for i in range(1, height + 1):
    # สร้างสตริงสำหรับแต่ละบรรทัดของปิรามิด
    line_list = []
    for j in range(i):
        # เลือกองค์ประกอบจาก data_list และคำนึงถึงการวนรอบ
        element = data_list[j % len(data_list)]
        line_list.append(element)
    
    # จัดเรียงสตริงสำหรับปิรามิด: จัดเรียงปกติและสลับด้าน
    line_string = ''.join(line_list + line_list[-2::-1])  # สร้างสตริงแบบสมมาตร
    
    # พิมพ์แต่ละแถวของปิรามิด, จัดกึ่งกลางด้วย rjust
    print(line_string.center(height * 2 - 1 + len(line_string)//2))
